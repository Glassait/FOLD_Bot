/**
 * Enum representing types of ammunition shells.
 */
export enum ShellEnum {
    /**
     * Armor-piercing ammunition.
     */
    ARMOR_PIERCING = 'AP',
    /**
     * Armor-piercing composite rigid ammunition.
     */
    ARMOR_PIERCING_CR = 'APCR',
    /**
     * High-explosive ammunition.
     */
    HIGH_EXPLOSIVE = 'HE',
    /**
     * Hollow charge ammunition.
     */
    HOLLOW_CHARGE = 'HEAT',
}

/**
 * Enum representing types of shell.
 */
export enum ShellType {
    /**
     * Gold (special) shell type.
     */
    GOLD = 'GOLD',
    /**
     * Normal shell type.
     */
    NORMAL = 'NORMAL',
}
